     private void Update_Table()
    {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3406/onlinevoting","root","somi");
            CallableStatement mycall = con.prepareCall("{call participation_ratio()}");
            mycall.execute();
            String sql = "select eligible_voters, voted from participation";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs= pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
         catch(Exception e)
        {
             JOptionPane.showMessageDialog(null, e); 
        }
    }
    
